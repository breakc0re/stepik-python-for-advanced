# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ü—Ä–∏–≤–µ–¥–µ–Ω–Ω—ã–µ –Ω–∏–∂–µ —Ç–µ—Å—Ç—ã —ç—Ç–æ –ª–∏—à—å –æ–±—Ä–∞–∑—Ü—ã –≤–æ–∑–º–æ–∂–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞. –í–æ–∑–º–æ–∂–Ω—ã –∏ –¥—Ä—É–≥–∏–µ —Å–ø–æ—Å–æ–±—ã
# –≤—ã–±–æ—Ä–∞ —Ç–∞–π–Ω—ã—Ö –¥—Ä—É–∑–µ–π.


import random

n = int(input())
students = [input() for _ in range(n)]
secret_friends = []

random.shuffle(students)

for i in range(n):
    if i != n - 1:
        secret_friends.append(students[i + 1])
    else:
        secret_friends.append(students[0])

for student, secret_friend in zip(students, secret_friends):
    print(f"{student} - {secret_friend}")
