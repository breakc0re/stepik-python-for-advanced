def josephus(n, k):
    # –°–æ–∑–¥–∞–µ–º —Å–ø–∏—Å–æ–∫ —Å –Ω–æ–º–µ—Ä–∞–º–∏ –ª—é–¥–µ–π –æ—Ç 1 –¥–æ n
    circle = list(range(1, n + 1))
    index = 0

    while len(circle) > 1:
        # –í—ã—á–∏—Å–ª—è–µ–º –∏–Ω–¥–µ–∫—Å —á–µ–ª–æ–≤–µ–∫–∞, –∫–æ—Ç–æ—Ä—ã–π –≤—ã–±—ã–≤–∞–µ—Ç –∏–∑ –∫—Ä—É–≥–∞
        index = (index + k - 1) % len(circle)
        # –£–¥–∞–ª—è–µ–º –≤—ã–±—ã–≤—à–µ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞ –∏–∑ —Å–ø–∏—Å–∫–∞
        circle.pop(index)

    return circle[0]  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–æ–º–µ—Ä –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –æ—Å—Ç–∞–≤—à–µ–≥–æ—Å—è —á–µ–ª–æ–≤–µ–∫–∞


# –°—á–∏—Ç—ã–≤–∞–µ–º –≤—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
n = int(input())
k = int(input())

# –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∏ –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
result = josephus(n, k)
print(result)
